package CONDITIONAL_STATEMENTS;

public class Switch_condition {
public static void main(String[] args) {
	String a = "india";
	switch (a) {
	case "china" :
		System.out.println("beging");
		break;
		
	case "japan":
		System.out.println("tokyo");
		break;
		
	case "india":
		System.out.println("delhi");
		break;
		default:
			System.out.println("default");
}

}
}
