public class relational_operators {
//this operator is used to the relation between the datas
	//there are ==,<,>,<=,>=,!=
	public static void main(String[] args) {
	 int a=10;
	 int b=40;
	 System.out.println(a<=b);
	 System.out.println(a!=b);
	 System.out.println(a>b);
	}
}
